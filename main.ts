function ch(note1: number) {
    music.playTone(note1, music.beat(BeatFraction.Quarter / 1.5))
}

function c(note: number) {
    music.playTone(note, music.beat(BeatFraction.Quarter))
}

function bpm(bpm: number) {
    music.setTempo(bpm)
}

function rest(fract: string) {
    if (fract == "8") {
        music.rest(music.beat(BeatFraction.Eighth))
    }
    
    if (fract == "2") {
        music.rest(music.beat(BeatFraction.Half))
    }
    
    if (fract == "16") {
        music.rest(music.beat(BeatFraction.Sixteenth))
    }
    
}

